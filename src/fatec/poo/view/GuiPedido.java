    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        
        if(tblItemPedido.getSelectedRow() == -1){
            
            JOptionPane.showMessageDialog(null, "Linha não selecionada", "Aviso", JOptionPane.ERROR_MESSAGE);
        }else{
            
            for(int i = 0; i < prod.size(); i++){
                if(prod.get(i).getCodigo().equals(tblItemPedido.getValueAt(tblItemPedido.getSelectedRow(), 0))){
                    prodselect = prod.get(i);
                }
                
               
            }
           
            prodselect.setQtdeEstoque(prodselect.getQtdeEstoque() + Double.parseDouble((String) tblItemPedido.getValueAt(tblItemPedido.getSelectedRow(), 3)));
            subtotal = Double.parseDouble((String)tblItemPedido.getValueAt(tblItemPedido.getSelectedRow(), 4));
            
            pedenc.voltaCredito(subtotal);
            total -= subtotal;
            
            lblValTotal.setText(String.valueOf(total));
            modTblPedido.removeRow(tblItemPedido.getSelectedRow());
            lblQtdeItensPedidos.setText(String.valueOf(modTblPedido.getRowCount()));
            
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        for (int i = 0; i < pes.size(); i++){
            if(pes.get(i).getCpf().equals(ftxCpfCliente.getText().replaceAll("[\\.\\-]", ""))){
                clienc = (Cliente) pes.get(i);
            }
        }
        
        
        DecimalFormat df = new DecimalFormat("#.##");
        
        if(txtQtdeVendida.getText().equals("0") || txtQtdeVendida.getText().equals("")){
            
            JOptionPane.showMessageDialog(null, "Dgite uma quantidade vendida valida", "Aviso", JOptionPane.ERROR_MESSAGE);
            
        }else if (Double.parseDouble(txtQtdeVendida.getText()) > prdenc.getQtdeEstoque()) {
                
            JOptionPane.showMessageDialog(null, "Quantidade em estoque insuficiente", "Aviso", JOptionPane.ERROR_MESSAGE);
            
            } else {
                
                objItemPedido = new ItemPedido(modTblPedido.getRowCount() + 1, Double.parseDouble(txtQtdeVendida.getText()), prdenc);
                                  //Codigo                Descrição               Prec Unit                                                 QtdeVend
                String linha[] = {txtCodProd.getText(), lblDescricao.getText(), String.valueOf(objItemPedido.getProduto().getPreco()), txtQtdeVendida.getText()};    
                
                subtotal = Double.parseDouble(txtQtdeVendida.getText()) * objItemPedido.getProduto().getPreco();
                if (clienc.getLimiteDisp() < subtotal) {
                    JOptionPane.showMessageDialog(null, "Limite insuficiente", "Aviso", JOptionPane.ERROR_MESSAGE);
                } else {
                    pedenc.addItem(objItemPedido);
                    modTblPedido.addRow(linha);
                    modTblPedido.setValueAt(df.format(subtotal), modTblPedido.getRowCount() - 1, 4);

                    total += subtotal;

                    lblValTotal.setText(String.valueOf(total));
                    lblQtdeItensPedidos.setText(String.valueOf(modTblPedido.getRowCount()));
                    
                    txtCodProd.setText(null);
                    lblDescricao.setText(" ");
                    txtQtdeVendida.setText("");
                    btnIncluir.setEnabled(true);
                    
                    btnAdicionar.setEnabled(false);
                }   
            }
    }//GEN-LAST:event_btnAdicionarActionPerformed

