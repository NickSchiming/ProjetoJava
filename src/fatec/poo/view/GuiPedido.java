    private void btnNumPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNumPedidoActionPerformed
        total = 0;
        
        DecimalFormat df = new DecimalFormat("#.##");
        
        if(ped.isEmpty()){
                    ftxDataPedido.setEnabled(true);
                    cbxFormaPagto.setEnabled(true);
                    ftxCpfCliente.setEnabled(true);

                    btnNumPedido.setEnabled(false);
                    btnCpfCliente.setEnabled(true);
                    btnAlterar.setEnabled(false);
                    btnExcluir.setEnabled(false);
                    
                    ftxDataPedido.setText(null);
                    cbxFormaPagto.setSelectedIndex(0);
                    ftxCpfCliente.setText(null);
                    lblNomeCliente.setText(" ");
                    ftxCpfVendedor.setText(null);
                    lblNomeVendedor.setText(" ");
                    txtCodProd.setText(null);
                    lblDescricao.setText(" ");
                    txtQtdeVendida.setText(null);
                    lblValTotal.setText(" ");
                    lblQtdeItensPedidos.setText(" ");
                    modTblPedido.setRowCount(0);
        }else{
            for (int i = 0; i < ped.size(); i++){

                if(ped.get(i).getNumero().equals(txtNumPedido.getText())){
                    System.out.println("Entrou no if " + txtNumPedido.getText());

                    pedenc = ped.get(i);

                    ftxDataPedido.setText(pedenc.getDataEmissao());
                    if(pedenc.getFormaPagto()){
                        cbxFormaPagto.setSelectedIndex(0);
                    }else{
                        cbxFormaPagto.setSelectedIndex(1);
                    }
                    
                    ftxCpfCliente.setText(pedenc.getCliente().getCpf());
                    lblNomeCliente.setText(pedenc.getCliente().getNome());
                    ftxCpfVendedor.setText(pedenc.getVendedor().getCpf());
                    lblNomeVendedor.setText(pedenc.getVendedor().getNome());

                    for(int j = 0; j < pedenc.getItensPedido().size(); j++){

                        subtotal = pedenc.getItensPedido().get(j).getQtdeVendida() * pedenc.getItensPedido().get(j).getProduto().getPreco();

                        String linha[] = {pedenc.getItensPedido().get(j).getProduto().getCodigo(),
                        pedenc.getItensPedido().get(j).getProduto().getDescricao(),
                        String.valueOf(pedenc.getItensPedido().get(j).getProduto().getPreco()),
                        String.valueOf(pedenc.getItensPedido().get(j).getQtdeVendida()), df.format(subtotal)};


                        modTblPedido.addRow(linha);

                        total += subtotal;

                        lblValTotal.setText(String.valueOf(total));
                        lblQtdeItensPedidos.setText(String.valueOf(modTblPedido.getRowCount()));

                    }

                    ftxDataPedido.setEnabled(false);
                    cbxFormaPagto.setEnabled(false);
                    ftxCpfCliente.setEnabled(false);
                    ftxCpfVendedor.setEnabled(false);
                    txtQtdeVendida.setEnabled(true);

                    btnCpfCliente.setEnabled(false);
                    btnCpfVendedor.setEnabled(false);
                    txtQtdeVendida.setEnabled(false);
                    tblItemPedido.setEnabled(false);
                    btnAlterar.setEnabled(true);
                    btnExcluir.setEnabled(true);
                    break;
                }else{
                    ftxDataPedido.setEnabled(true);
                    cbxFormaPagto.setEnabled(true);
                    ftxCpfCliente.setEnabled(true);

                    btnNumPedido.setEnabled(false);
                    btnCpfCliente.setEnabled(true);
                    btnAlterar.setEnabled(false);
                    btnExcluir.setEnabled(false);
                    
                    ftxDataPedido.setText(null);
                    cbxFormaPagto.setSelectedIndex(0);
                    ftxCpfCliente.setText(null);
                    lblNomeCliente.setText(" ");
                    ftxCpfVendedor.setText(null);
                    lblNomeVendedor.setText(" ");
                    txtCodProd.setText(null);
                    lblDescricao.setText(" ");
                    txtQtdeVendida.setText(null);
                    lblValTotal.setText(" ");
                    lblQtdeItensPedidos.setText(" ");
                    modTblPedido.setRowCount(0);
                }
            }
        }
    }//GEN-LAST:event_btnNumPedidoActionPerformed

    private void btnCpfClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCpfClienteActionPerformed
        pedenc = new Pedido(txtNumPedido.getText(), ftxDataPedido.getText().replaceAll("/", ""));
        if(cbxFormaPagto.getSelectedIndex() == 0){
            pedenc.setFormaPagto(true);
        }else{
            pedenc.setFormaPagto(false);
        }
        clienc = null;

        for (int i = 0; i < pes.size(); i++){
            if(pes.get(i).getCpf().equals(ftxCpfCliente.getText().replaceAll("[\\.\\-]", ""))){
                clienc = (Cliente) pes.get(i);
                
                pedenc.setCliente((Cliente) clienc);
                
                lblNomeCliente.setText(clienc.getNome());

                ftxDataPedido.setEnabled(false);
                cbxFormaPagto.setEnabled(false);
                ftxCpfCliente.setEnabled(false);
                btnCpfCliente.setEnabled(false);
                ftxCpfVendedor.setEnabled(true);
                btnCpfVendedor.setEnabled(true);
            }
        }
        
        if(clienc == null){
            JOptionPane.showMessageDialog(null, "A pessoa procurada não é um cliente", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnCpfClienteActionPerformed

